package com.ddxygq.ssmdemo.pojo;

public class Person {

	private String person_id;
	
	private String person_name;
	
	private String person_account;
	
	private String person_pwd;
	
	private String person_role;
	
	private String person_flag;
	
	private String person_address;
	
	private String person_head;

	public String getPerson_id() {
		return person_id;
	}

	public void setPerson_id(String person_id) {
		this.person_id = person_id;
	}

	public String getPerson_name() {
		return person_name;
	}

	public void setPerson_name(String person_name) {
		this.person_name = person_name;
	}

	public String getPerson_account() {
		return person_account;
	}

	public void setPerson_account(String person_account) {
		this.person_account = person_account;
	}

	public String getPerson_pwd() {
		return person_pwd;
	}

	public void setPerson_pwd(String person_pwd) {
		this.person_pwd = person_pwd;
	}

	public String getPerson_role() {
		return person_role;
	}

	public void setPerson_role(String person_role) {
		this.person_role = person_role;
	}

	public String getPerson_flag() {
		return person_flag;
	}

	public void setPerson_flag(String person_flag) {
		this.person_flag = person_flag;
	}

	public String getPerson_address() {
		return person_address;
	}

	public void setPerson_address(String person_address) {
		this.person_address = person_address;
	}

	public String getPerson_head() {
		return person_head;
	}

	public void setPerson_head(String person_head) {
		this.person_head = person_head;
	}
	
}
