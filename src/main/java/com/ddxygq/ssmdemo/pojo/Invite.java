package com.ddxygq.ssmdemo.pojo;

public class Invite {

	private String invite_id;
	
	private String invite_worker;
	
	private String invite_order;
	
	private String invite_flag;
	
	private String invite_head;

	public String getInvite_id() {
		return invite_id;
	}

	public void setInvite_id(String invite_id) {
		this.invite_id = invite_id;
	}

	public String getInvite_worker() {
		return invite_worker;
	}

	public void setInvite_worker(String invite_worker) {
		this.invite_worker = invite_worker;
	}

	public String getInvite_order() {
		return invite_order;
	}

	public void setInvite_order(String invite_order) {
		this.invite_order = invite_order;
	}

	public String getInvite_flag() {
		return invite_flag;
	}

	public void setInvite_flag(String invite_flag) {
		this.invite_flag = invite_flag;
	}

	public String getInvite_head() {
		return invite_head;
	}

	public void setInvite_head(String invite_head) {
		this.invite_head = invite_head;
	}
	
}
